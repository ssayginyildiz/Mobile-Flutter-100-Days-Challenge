1. Gün: Dart'a Giriş
Dart Kurulumu: Dart SDK'yı indirip kurarak geliştirme ortamını hazırlayın.
Temel Kavramlar: Dart dilinin temel yapı taşlarını öğrenin.
Değişkenler ve Veri Tipleri: int, double, String, bool, var gibi veri tiplerini keşfedin.
Operatörler: Matematiksel (+, -, *, /) ve mantıksal (&&, ||) operatörler.
2. Gün: Kontrol Yapıları
Koşullar: if, else, else if kullanımı.
Döngüler: for, while, do-while döngüleri.
Kodlama Alıştırmaları: Basit döngüler ve koşullu ifadelerle küçük projeler yapın.
3. Gün: Fonksiyonlar
Fonksiyon Tanımı ve Kullanımı: Fonksiyonları nasıl tanımlayıp çağıracağınızı öğrenin.
Parametreler ve Geri Dönüş Değerleri: Fonksiyonlara parametre geçişini ve geri dönüş değerlerini anlayın.
Lambda Fonksiyonları: Anonim fonksiyonların (lambda fonksiyonları) nasıl kullanıldığını öğrenin.
4. Gün: Koleksiyonlar
Listeler: Dart'taki listeleri (List) öğrenin ve basit liste işlemleri yapın.
Map ve Set: Dart'taki Map ve Set yapılarını inceleyin, veri saklama ve erişimi deneyin.
Listelerde İleri İşlemler: Listelerde filtreleme, sıralama, map ve reduce işlemlerini uygulayın.
5. Gün: Nesne Yönelimli Programlama (OOP)
Sınıflar ve Nesneler: Sınıf nasıl tanımlanır, nesne nasıl oluşturulur öğrenin.
Kapsülleme: Sınıf üyelerine erişimi sınırlandırmak için private erişim belirleyicilerini kullanın.
Yapıcı Metotlar: Sınıfın ilk değerlerini ayarlamak için yapıcı (constructor) metotları kullanın.
6. Gün: Miras ve Polimorfizm
Miras: Kalıtım (inheritance) ile sınıflar arası ilişkileri öğrenin.
Polimorfizm: Metodun aşırı yüklenmesi (method overriding) ve çok biçimlilik konularını öğrenin.
Abstract Sınıflar ve Interface: Abstract sınıflar ve interface kullanarak yapılandırma yapın.
7. Gün: Asenkron Programlama
Future ve async-await: Asenkron işlemleri nasıl yöneteceğinizi öğrenin.
Stream: Akışlar (streams) ile veri akışını yönetin.
Uygulamalı Alıştırmalar: API çağrıları veya zamanlayıcılarla asenkron programlama pratiği yapın.
8. Gün: Hata Yönetimi ve Test
Error Handling: Dart'ta hata yönetimi nasıl yapılır, try-catch, throw gibi yapıları öğrenin.
Test Yazımı: Dart'ta birim testleri (unit tests) nasıl yazılır, assert ve test paketlerini inceleyin.
Debugging Teknikleri: Debugging araçlarını ve tekniklerini kullanarak hataları tespit edin.
9. Gün: Paket Kullanımı ve İleri Konular
Paket Kullanımı: Dart'ta paket (package) nasıl yüklenir ve kullanılır öğrenin (pub.dev).
Generics: Tür güvenliğini artırmak için generic yapıları kullanın.
Mixin ve Extension: Kodunuzu yeniden kullanmak için mixin'leri ve extension'ları inceleyin.
10. Gün: Proje Geliştirme
Küçük Bir Proje Geliştirin: Öğrendiklerinizi uygulamak için bir proje geliştirin.
To-Do List veya Basit Hesap Makinesi gibi bir uygulama.
OOP, Koleksiyonlar, Asenkron Programlama ve Paket Kullanımı konularını projede kullanın.
Proje İncelemesi: Kendi kodunuzu gözden geçirip, performans ve optimizasyon açısından değerlendirin.
